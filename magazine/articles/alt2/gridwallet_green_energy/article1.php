<p>
Alt2 Media interviewed Mike Newman from Grid Wallet.
</p>

<div class="article-break">Grid Wallet - what is it and how did you come up with the idea?</div>

<p>
Grid Wallet is a green energy market place for green electricity. It enables you to trade the electricity you produce from your solar panels (or any power source) with your neighbours. It represents the future service that energy utilities will be providing as we move towards a distributed energy system. For example, the more solar panels people install on their properties, the less electricity they will demand from large scale sources like nuclear power plants. Energy production is becoming localised.
</p>

<p>
I came up with the idea when I saw the anxiety that it caused my father (a pensioner) when he didn't use all of his solar power during the day.
</p>

<p>
He changed his behaviour to use different electronic devices (like the washing machine) during daylight hours, rather than facing the low prices he would be given for his unused solar energy. But this was not easy for him and caused him stress.
</p>

<p>
Great green innovations do not ask us to change our behaviour, but instead improve our lifestyles and the environment for little or no extra cost. Just look at what Tesla has achieved with electric cars, solar panels and batteries. At no point does he ask you to change your behaviour!
</p>

<div class="article-break">And what would motivate the electric companies to cooperate with you?</div>

<p>
Good question. We are asking them to take a hit on their profit margin for our initial trial so initially it doesnâ€™t sound like a good deal. However, as soon as the concept is proven and they decide to roll out more widely, the prosumer market in the UK suddenly will see the value in the service and we expect a large amount of them to switch over. Once you have them, it is likely you will keep them for a long time (solar panels typically last 15-20 years). Currently customer switching happens a lot more frequently than utilities would like. So we are selling a vision for future growth of their company. Utility companies must also realise that a decentralised energy system is leading to a change in the type of service they offer and we can help them transform to offer this service.
</p>

<div class="article-break">How will your customers use Grid Wallet? On their phone or on their computer?</div>

<p>
Customers will have the choice of accessing the marketplace on any device, however we envisage that the majority of users will not need regular mobile access.
</p>

