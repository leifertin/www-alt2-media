<p>
Alt2 Media sat down with Sankar Gopalraj from Ilai Eco-Bicycles at *ship Festival 2017 in Kotka, Finland.
</p>

<div class="article-break">How did you come up with your idea?</div>

<p>
I was completing my first year of studies and for the summer I was jobless, but before that I was working 8 months in a startup company called Norland Cycles. I am a Design Engineer there and my background is Failure Analysis and Material Science. So I put my studies together with what I love.
</p>

<p>
I found this idea when I was riding a bike. What if a bicycle was made out of plastic? I was doing some benchmarking based on this and I found just two competitors. One is from Brazil - they are just doing frames. The other is from the Netherlands, making tanks but they are calling them bicycles which is not believable. Still they are making things from plastics.
My idea is to make a bicycle out of plastic, make it affordable, safe, and durable. That is my target.
</p>

<div class="article-break">You told us about your studies, so what exactly are you studying?</div>

<p>
My Bachelors, I did in India, Mechanical Engineering in Design. Masters in Mechanical Engineering of Fine Element Analysis and Fabric Design so basically whatever component you give me, I can analyse what it can and cannot withstand.
</p>

<div class="article-break">And you studied that in Lappeenranta?</div>

<p>
Yeah, Lappeenranta University of Technology.
</p>

<div class="article-break">How did you end up in Finland?</div>

<p>
After finishing my Bachelors, I started to study German. I needed a break from the cycle of education and work. I wanted to learn a language so I ended up learning German. In that eight months, some of my friends motivated me to apply for universities in Germany and got admitted to three of them.
Then I applied to Finland, just to Lappeenranta, because in case I didn’t get an opportunity in Germany, it was a backup option. But the thing was some of my friends said I should go to Finland. I learned the German language but let me go to Finland because they speak English, so I have two options. Why constrain myself to just one country? That is how I ended up in Finland.
</p>

<div class="article-break">What have you planned for after your studies? Going back to India or travelling the world?</div>

<p>
The thing is, I have my study rights for four years. I started last year and I finished my Masters Degree. I almost did 85 credits, just my thesis left. Now I am planning to some internships in a company because I feel like just theoretical knowledge doesn’t give you much experience so I want to apply it practically. For the entire summer I applied for internships in hope of a chance of getting into one company. I have the final interview on the 25th of [July]. That’s a one-year internship and hopefully I’ll continue. So I’ll just stop my studies, continue the internship and then come back and do my studies.
</p>

<div class="article-break">So write your thesis after?</div>

<p>
Yes, I will write the thesis with them.
</p>

<div class="article-break">And after that?</div>

<p>
After that I am planning to stay in Europe and work; Not for a long time, 5 to 6 years, and then I would like to go back to India, and apply what I have learnt from my experience.
</p>

<div class="article-break">Maybe create the bicycles there?</div>

<p>
No, I want to create the bicycles in Europe.
</p>

<div class="article-break">Why exactly Europe?</div>

<p>
Because “Made in Europe” is quality. “Made in Germany” we trust in India. “Made in China” do you trust in Germany?
But maybe I can draw the raw materials from India, manufacture in China, then ship here and do the marketing here.
</p>

<div class="article-break">Then there’s also huge amounts of plastic floating in the ocean.</div>

<p>
Yeah, we actually thought about motivating the guys from The States because they really don’t give a shit about nature. Maybe because of the Trump or something else but I’m not offending them, sorry. But still I think US should move towards green technology.
</p>