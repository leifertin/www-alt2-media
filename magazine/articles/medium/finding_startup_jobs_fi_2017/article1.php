</p>

<p class="article-quote">
“I’m moving from <b>[pick a small Finnish city]</b> to Helsinki and would like to start working in a startup. Where to find the jobs?”
</p>

<p>
These are the two questions I get asked a lot. Slush and the startup communities have done their work and that has been visible for a while now but during the recent year many people I know have been jumping off the corporate bandwagon and are looking towards startups. And the reasons are obvious.
</p>

<p>
People are more and more looking to do something they can find deeper meaning and connection to than work in a big company which might or might not be able to create a common vision towards something bigger than the monthly payroll. In addition for many the corporate ladder seems to be too slow and people are thirsty for more responsibilities faster than it used to be and startup are able to provide with that promise. Biggest factor of course is that the job creation ability of big companies has been severed and there is no indication for better.
</p>

<div class="article-break">“You get to work in a startup, and you get to work in a startup. Everyone gets to work in a startup!”</div>

<p>
Let’s jump to the moment when you have decided that startups is the way to go for you. Where should you start looking?
</p>

<p>
I’d say that for me there are four main channels you should be actively looking into: Startup events, startup job bulletins, LinkedIn and Finnish Startup100 -listing by people behind <a href="https://sanfrancisco.fi/fi/" target="_blank">San Francisco</a>.
</p>

<div class="article-break">Startup Events</div>

<p>
Snatching a job in a early stage startup with relatively small team is not only about your skills. In addition to your talent the willingness & ability to learn and cultural fit are the defining factors on your recruitability to a startup. That’s why getting to know the possible candidates outside work context is extremely important. In each of the big cities you can find the startup societies, co-working spaces and few persons who are in the epicenter of all the startup happenings. These are the places you should regularly visit and people you should get to know.
</p>

<p>
<b>Step One</b>: Open that Facebook, Meetup.com or start googling away.<br> 
<b>Step Two</b>: Attend the events and get to know people.<br>
<b>TIP</b>: Always look for a way to help other people — not how others can help your agenda. <i>#givefirst</i>
</p>

<div class="article-break">Helsinki</div>

<p>
You should definitely look into what’s happening in <a href="http://www.maria.io/" target="_blank">Maria 01</a>, <a href="http://www.microsoftflux.com/" target="_blank">Microsoft Flux</a>, <a href="https://aaltoes.com/" target="_blank">Aaltoes</a>, <a href="http://startupsauna.com/" target="_blank">Startup Sauna</a> and different meetup groups and start from there. <a href="http://www.newcohelsinki.fi/" target="_blank">NewCoHelsinki</a> is occasionally organizing a <i>“Find a Co-founder”</i> event so check that out too. Find Maria’s <a href="https://twitter.com/ErikaHalonen" target="_blank">Erika Halonen</a>, <a href="https://twitter.com/MSFTFlux" target="_blank">Microsoft Flux</a>, <a href="https://twitter.com/aaltoes" target="_blank">Aaltoes</a> and <a href="https://twitter.com/startupsauna" target="_blank">Startup Sauna</a> from Twitter, Facebook and Instagram and get your personal pitch ready.
</p>

<div class="article-break">Turku</div>

<p>
Turku and startups equals <a href="http://boostturku.com/" target="_blank">Boost</a>. Also <a href="https://www.sparkup.fi/en" target="_blank">SparkUp</a>, newly opened <a href="http://hubturku.com" target="_blank">Hub Turku</a> and <a href="http://turkugamehub.com/" target="_blank">Turku Game Hub Hive</a> are the places to check out. <a href="https://twitter.com/Ketopaikka" target="_blank">Anssi</a> &amp; <a href="https://twitter.com/Hamatti" target="_blank">Juhis</a> from Boost and <a href="https://twitter.com/LeoKadieff" target="_blank">Leo</a> from <a href="http://www.utu.fi" target="_blank">University of Turku</a> are the persons who can guide you in Turku ecosystem so be sure to ping them.
</p>

<div class="article-break">Oulu</div>

<p>
Oulu ecosystem is currently in a state of change as their main startup hub <a href="https://www.businesskitchen.fi/" target="_blank">Business Kitchen</a> was scattered into three smaller co-working spaces around the city and <a href="http://pelikampus.fi/" target="_blank">Fingersoft’s Pelikampus</a> is just starting up. If you happen to be in Oulu you should also ping <a href="https://twitter.com/speville" target="_blank">Jean Speville</a> from <a href="http://www.vessla.net/" target="_blank">Vessla</a>, The founder of <a href="https://www.asmosolutions.com/" target="_blank">Asmo Solutions</a> <a href="https://twitter.com/AsmoSaloranta" target="_blank">Asmo Saloranta</a> and <a href="https://twitter.com/AnneSorvari" target="_blank">Anne Sorvari</a> as they are highly connected in Oulu. Vessla and Asmo are also running a small private shared office in downtown Oulu which has always welcomed new friendly entrepreneurs to stop for a coffee and chat.
</p>