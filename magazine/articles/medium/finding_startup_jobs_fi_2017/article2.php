<div class="article-break">Tampere</div>

<p>
Tampere from my perspective looks a bit dim currently. The old Finlayson area with <a href="http://www.newfactory.fi/" target="_blank">NewFactory</a> has been under turmoil with diminishing resources which has not had positive effect to the scene. Fortunately the newly opened privately run <a href="http://www.startuphub.fi/" target="_blank">Tampere Startup Hub</a> works currently as a beacon for something which hopefully in the coming years will become the renaissance of Tampere startup scene.
</p>

<div class="article-break">Job Sites</div>

<p>
Recruiting platforms are of course one crucial part of finding the right startup to join. <a href="https://thehub.fi/" target="_blank">The Hub</a> is the newest entry to Finnish market and is run by <a href="http://maria.io/" target="_blank">Maria01</a> and <a href="https://www.danskebank.fi" target="_blank">Danske Bank</a>. Even though it was just launched it seems to be gathering speed pretty fast.
</p>

<p>
One good source to look into is venture capital firm <a href="http://www.inventure.fi" target="_blank">Inventure</a>’s web page <a href="http://www.inventure.fi/jobs" target="_blank">job section</a> where they are listing open positions in their portfolio companies (currently over 100 positions!). Other platforms where you can find startup jobs are <a href="http://www.finlandstartupjobs.com/" target="_blank">FinlandStartupJobs</a>, the <a href="http://startup.jobilla.com/" target="_blank">job platfrom by Arctic Startup</a> and <a href="http://jobs.startup100.net/" target="_blank">Startup100 Jobs</a>. <a href="https://angel.co/helsinki/jobs" target="_blank">Angellist</a> has also some job postings from Finnish startups.
</p>

<div class="article-break">LinkedIn</div>

<p>
Of course in 2017 when talking about jobs you can’t miss <a href="http://linkedin.com/" target="_blank">LinkedIn</a>. Many of the companies are using their people as part of their recruiting engine and you can find multiple persons with “we are hiring” -texts in their description.
</p>

<p>
So spin up that LinkedIn search and look with the right keywords with geographical limitations of your choice. If your LinkedIn network is not that broad getting that one month free Premium trial or paying for it might be worth the investment as Premium enables you to search also 3rd degree contacts.
</p>

<p>
Remember also to make your LinkedIn page shine before reaching out. Hubspot has created a good post on starting the journey to perfect Linkedin profile <a href="https://blog.hubspot.com/marketing/linkedin-profile-perfection-cheat-sheet#sm.001dguvn5llyeuz11nb1npgg0uwx4" target="_blank">here</a>.
</p>

<div class="article-break">Startup100</div>

<p>
One good source for spotting your perfect startup job is to look into the listing of <a href="http://startup100.net/" target="_blank">Startup100.net</a> — Startup100 lists the Finnish startups in their database monthly according to visibility in media. This is a good place to start finding the startup you really are interested in and working your way towards getting that next opening from them when the time is right.
</p>

<p>
This was my short listing for all of you looking to secure that dream startup job of yours. Hopefully this information will be helpful for you and you get that position.
</p>

